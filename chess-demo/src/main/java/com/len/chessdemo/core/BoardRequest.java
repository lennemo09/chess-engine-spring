package com.len.chessdemo.core;

public class BoardRequest {
    public String boardId;
    public int fromX;
    public int fromY;
    public int toX;
    public int toY;

    public String getBoardId() {
        return boardId;
    }

    public int getFromX() {
        return fromX;
    }

    public int getFromY() {
        return fromY;
    }

    public int getToX() {
        return toX;
    }

    public int getToY() {
        return toY;
    }
}
